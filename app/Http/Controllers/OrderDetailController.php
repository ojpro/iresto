    {
		$request->validate([
			'client_id'=>['required','exists:clients,id'],
			'orders'=>['required','array']
		]);

		$order_details = OrderDetail::create([
			'client_id'=>$request->client_id,
		]);

		/*
		 *
		 * Insert Orders
		 */

	    foreach($request->orders as $order){
		    OrderController::store($order, $order_details-id);
	    }
    }

